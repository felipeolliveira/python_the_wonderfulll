#
# Arithmetic Operators in Python
#
add = 2 + 2
subtract = 2 - 2
multiply = 2 * 2
divide = 2 / 2

# This operator performs floor division, which means it returns the largest integer less than or equal to the result of the division.
floor_divide = 55 // 2

# The modulo operator returns the remainder of the division of the left operand by the right operand.
modulo = 2 % 2

# The exponentiation operator raises the left operand to the power of the right operand.
exponent = 2**2

print("\n\nArithmetic Operations:")
print(f"Addition: {add}, Type: {type(add)}")
print(f"Subtraction: {subtract}, Type: {type(subtract)}")
print(f"Multiplication: {multiply}, Type: {type(multiply)}")
print(f"Division: {divide}, Type: {type(divide)}")
print(f"Floor Division: {floor_divide}, Type: {type(floor_divide)}")
print(f"Modulo: {modulo}, Type: {type(modulo)}")
print(f"Exponentiation: {exponent}, Type: {type(exponent)}")
